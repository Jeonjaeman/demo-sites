/* ------------------------ 시드 기반 결정론적 응답 생성기 ------------------------ */
function mulberry32(a){ return function(){ a|=0; a=a+0x6D2B79F5|0; let t=Math.imul(a^a>>>15,1|a); t=t+Math.imul(t^t>>>7,61|t)^t; return ((t^t>>>14)>>>0)/4294967296; }; }

/* bias: 요인별 응답 성향 (-1.6 ~ +1.6). 응답 = 3 + bias + noise → 1~5 클램프
   역채점 문항은 방향을 뒤집어 생성 → 채점 엔진이 되돌렸을 때 일관된 프로파일이 나옴 */
function genAnswers(seed, bias, opts={}){
  const rnd = mulberry32(seed);
  const ans = {};
  ITEMS.forEach(it=>{
    const b = bias[it.f] ?? 0;
    const dir = it.r ? -1 : 1;
    const noise = (rnd()*2 - 1) * 0.9;
    let v = Math.round(3 + dir*b + noise);
    v = Math.max(1, Math.min(5, v));
    ans[it.id] = v;
  });
  if(opts.missing) opts.missing.forEach(id=> delete ans[id]);          // 응답 누락
  if(opts.invalid) Object.entries(opts.invalid).forEach(([id,v])=> ans[id]=v); // 범위 이탈
  return ans;
}

